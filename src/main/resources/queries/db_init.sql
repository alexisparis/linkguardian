CREATE TABLE jhi_persistent_audit_evt_data (
    event_id bigint NOT NULL,
    name character varying(255) NOT NULL,
    value character varying(255)
);

CREATE TABLE jhi_persistent_token (
    series character varying(255) NOT NULL,
    user_id bigint,
    token_value character varying(255) NOT NULL,
    token_date date,
    ip_address character varying(39),
    user_agent character varying(255)
);

CREATE TABLE jhi_social_user_connection (
    id bigint NOT NULL,
    user_id character varying(255) NOT NULL,
    provider_id character varying(255) NOT NULL,
    provider_user_id character varying(255) NOT NULL,
    rank bigint NOT NULL,
    display_name character varying(255),
    profile_url character varying(255),
    image_url character varying(255),
    access_token character varying(255) NOT NULL,
    secret character varying(255),
    refresh_token character varying(255),
    expire_time bigint
);


CREATE TABLE jhi_user (
    id bigint NOT NULL,
    login character varying(100) NOT NULL,
    password_hash character varying(60),
    first_name character varying(50),
    last_name character varying(50),
    email character varying(100),
    activated boolean NOT NULL,
    lang_key character varying(5),
    activation_key character varying(20),
    reset_key character varying(20),
    created_by character varying(50) NOT NULL,
    created_date timestamp without time zone NOT NULL,
    reset_date timestamp without time zone,
    last_modified_by character varying(50),
    last_modified_date timestamp without time zone
);


CREATE TABLE jhi_user_authority (
    user_id bigint NOT NULL,
    authority_name character varying(50) NOT NULL
);


CREATE TABLE link (
    id bigint NOT NULL,
    creation_date timestamp without time zone NOT NULL,
    description character varying(255),
    domain character varying(255) NOT NULL,
    locked boolean,
    note integer,
    read boolean,
    title character varying(255),
    url character varying(255) NOT NULL,
    user_id bigint,
    original_url text
);

CREATE TABLE link_tag (
    tags_id bigint NOT NULL,
    links_id bigint NOT NULL
);


CREATE TABLE tag (
    id bigint NOT NULL,
    label character varying(255) NOT NULL
);




